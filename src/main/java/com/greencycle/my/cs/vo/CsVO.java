package com.greencycle.my.cs.vo;

public class CsVO {
	private int csNo;
	private String csTitle;
	private String csContent;
	private String userId;
	private String userNick;
	private String csDate;
	private String csPw; // 비밀글 여부
	private String csCategoryNm;
	private String csCategory;
	public int getCsNo() {
		return csNo;
	}
	public void setCsNo(int csNo) {
		this.csNo = csNo;
	}
	public String getCsTitle() {
		return csTitle;
	}
	public void setCsTitle(String csTitle) {
		this.csTitle = csTitle;
	}
	public String getCsContent() {
		return csContent;
	}
	public void setCsContent(String csContent) {
		this.csContent = csContent;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserNick() {
		return userNick;
	}
	public void setUserNick(String userNick) {
		this.userNick = userNick;
	}
	public String getCsDate() {
		return csDate;
	}
	public void setCsDate(String csDate) {
		this.csDate = csDate;
	}
	public String getCsPw() {
		return csPw;
	}
	public void setCsPw(String csPw) {
		this.csPw = csPw;
	}
	public String getCsCategoryNm() {
		return csCategoryNm;
	}
	public void setCsCategoryNm(String csCategoryNm) {
		this.csCategoryNm = csCategoryNm;
	}
	public String getCsCategory() {
		return csCategory;
	}
	public void setCsCategory(String csCategory) {
		this.csCategory = csCategory;
	}
	@Override
	public String toString() {
		return "CsVO [csNo=" + csNo + ", csTitle=" + csTitle + ", csContent=" + csContent + ", userId=" + userId
				+ ", userNick=" + userNick + ", csDate=" + csDate + ", csPw=" + csPw + ", csCategoryNm=" + csCategoryNm
				+ ", csCategory=" + csCategory + "]";
	}
	
	
}
